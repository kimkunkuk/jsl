package dto;

public class Tdto {
	String name, area;
	int age;
	
	public Tdto(String name, String area, int age) {
		super();
		this.name = name;
		this.area = area;
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	public String getArea() {
		return area;
	}
	public int getAge() {
		return age;
	}
	
	
}
